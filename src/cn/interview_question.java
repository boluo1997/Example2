//synchronized关键字，是怎么保证线程安全的呢
/**
 * 在Java中所有的对象，都会有一把锁，叫做内置锁，也称作监视器锁。这是种排他锁。
 *
 * 排他锁：一个线程获取后，其他线程只能等待其释放后，才有机会获得该锁
 *
 * Java中每个对象，都可以把内置锁，当做一个同步锁来使用。
 * 当一个线程在进入到synchronized代码块前时，会自动获取到监视器锁，
 * 此时其他线程在访问synchronized代码块时，就会被堵塞挂起（被拒之门外的意思）。
 *
 * 拿到锁的线程会在执行完成、或者抛出异常、或者调用wait系列方法时释放该锁。
 * 其他线程只能等待锁被释放后才能获取该锁。
 *
 * 通俗的讲，synchronized关键字将代码块中代码由并行转变成了串行，这样就保证了代码被顺序执行。
 */

